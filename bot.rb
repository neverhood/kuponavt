class String
  def downcase
    Unicode.downcase self
  end

  def include_pattern?(pattern)
    if pattern.is_a? Array
      pattern.each { |piece| return false unless self.include? piece }
      true
    else
      self.include? pattern
    end
  rescue Exception => e
    binding.pry
  end

end

